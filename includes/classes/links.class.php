				}
				if (urlencode($post_post['url'][$link['sectionID']]) != $post_post['url'][$link['sectionID']]) {
					$errors .= 'Error in URL:'.$post_post['url'][$link['sectionID']].'<br />';
				}
			}
		}
		$mysql->build_array('SELECT * FROM sections WHERE (type = 2 OR type = 3) AND hidden = 0');
