	function edit_extra_page() {
		$post_post = remove_slashes($_POST);
		$mysql = new mysql;
		$q = 'UPDATE sections SET ';
		$q .= 'name = \''.mysql_real_escape_string($post_post['page_name']).'\', ';
		$q .= 'url = \''.$post_post['page_url'].'\', ';
		$q .= 'body = \''.mysql_real_escape_string($post_post['page_body']).'\', ';
		if ($post_post['include_in_links']) {
			$q .= 'hidden = 0 ';
		} else {
			$q .= 'hidden = 1 ';
		}
		$q .= ' WHERE sectionID = '.$GLOBALS['page'].' LIMIT 1';
		
		if ($mysql->query($q)) {
			return true;
		} else {
			$this->error = $mysql->error;
			return false;
		}
	}
	
