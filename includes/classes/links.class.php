			foreach($mysql->result as $link) {
				if (!$post_post['name'][$link['sectionID']] or !$post_post['url'][$link['sectionID']]) {
					$errors .= 'Missing Field in '.ucwords(MEMBERS_NAME_SINGULAR).'/'.ucwords(ADMIN_NAME_SINGULAR).' Links<br />';
